"""
==============================================
  MODULE 1 - LESSON 1: Think Like a Programmer
==============================================

This program shows why CLEAR instructions matter!

A computer does EXACTLY what you tell it to do.
If your instructions are unclear, funny things happen!

Run this file to see the examples.
"""

# --------------------------------------------------
# Example 1: Clear vs. Unclear Instructions
# --------------------------------------------------

print("=" * 50)
print("  THINK LIKE A PROGRAMMER!")
print("=" * 50)
print()

# Let's pretend we are giving a robot instructions
# to draw a square.

print("UNCLEAR INSTRUCTIONS:")
print("  'Hey robot, draw a square.'")
print("  Robot says: 'How big? Where? What color?'")
print("  The robot is confused!")
print()

print("CLEAR INSTRUCTIONS:")
print("  1. Pick up the blue pen.")
print("  2. Go to the center of the paper.")
print("  3. Draw a line 5 cm to the right.")
print("  4. Draw a line 5 cm down.")
print("  5. Draw a line 5 cm to the left.")
print("  6. Draw a line 5 cm up.")
print("  The robot draws a perfect square!")
print()

# --------------------------------------------------
# Example 2: Order Matters!
# --------------------------------------------------

print("-" * 50)
print("ORDER MATTERS!")
print("-" * 50)
print()

print("Making cereal the WRONG way:")
print("  1. Pour the milk.")
print("  2. Get a bowl.")
print("  3. Pour the cereal.")
print("  Result: Milk is all over the table!")
print()

print("Making cereal the RIGHT way:")
print("  1. Get a bowl.")
print("  2. Pour the cereal.")
print("  3. Pour the milk.")
print("  Result: A perfect bowl of cereal!")
print()

# --------------------------------------------------
# Key Takeaway
# --------------------------------------------------

print("=" * 50)
print("KEY TAKEAWAY:")
print("  Computers follow instructions EXACTLY.")
print("  Be clear. Be specific. Put things in order.")
print("  That's what programming is all about!")
print("=" * 50)
